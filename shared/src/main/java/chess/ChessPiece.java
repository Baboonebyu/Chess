package chess;
import java.util.Collection;
import java.util.Objects;

/**
 * Represents a single chess piece
 * <p>
 * Note: You can add to this class, but you may not alter
 * signature of the existing methods.
 */
public class ChessPiece {

    private final ChessGame.TeamColor pieceColor;
    private final PieceType type;

    public ChessPiece(ChessGame.TeamColor pieceColor, PieceType type) {
        this.pieceColor = pieceColor;
        this.type = type;
    }


     //The various different chess piece options
    public enum PieceType {
        KING,
        QUEEN,
        BISHOP,
        KNIGHT,
        ROOK,
        PAWN
    }

    //@return Which team this chess piece belongs to
    public ChessGame.TeamColor getTeamColor() {
        return pieceColor;
    }


    //@return which type of chess piece this piece is

    public PieceType getPieceType() {
        return type;
    }

    @Override
    public boolean equals(Object o) {
        if (o == null || getClass() != o.getClass()) {
            return false;
        }
        ChessPiece piece = (ChessPiece) o;
        return pieceColor == piece.pieceColor && type == piece.type;
    }

    @Override
    public int hashCode() {
        return Objects.hash(pieceColor, type);
    }

    /**
     * Calculates all the positions a chess piece can move to
     * Does not take into account moves that are illegal due to leaving the king in
     * danger
     *
     * @return Collection of valid moves
     */


    public Collection<ChessMove> pieceMoves(ChessBoard board, ChessPosition myPosition) {

        MoveCalculator moves = new MoveCalculator(board, myPosition);
        return moves.pieceCalc();
    }

    @Override
    public String toString() {
        if(pieceColor == ChessGame.TeamColor.WHITE) {
            if (type == PieceType.KING) {return "K";}
            else if (type == PieceType.QUEEN) {return "Q";}
            else if (type == PieceType.BISHOP) {return "B";}
            else if (type == PieceType.KNIGHT) {return "N";}
            else if (type == PieceType.ROOK) {return "R";}
            else  {return "P";}
        }
        else {
            if (type == PieceType.KING) {return "k";}
            else if (type == PieceType.QUEEN) {return "q";}
            else if (type == PieceType.BISHOP) {return "b";}
            else if (type == PieceType.KNIGHT) {return "n";}
            else if (type == PieceType.ROOK) {return "r";}
            else  {return "p";}
            }
        }



}
